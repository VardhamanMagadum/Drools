package com.ruleProject.model;

public class Person {

	private int ficoScore;
	private String defaultChances;

	public int getFicoScore() {
		return ficoScore;
	}

	public void setFicoScore(int ficoScore) {
		this.ficoScore = ficoScore;
	}

	public String getDefaultChances() {
		return defaultChances;
	}

	public void setDefaultChances(String defaultChances) {
		this.defaultChances = defaultChances;
	}

}